#ifndef _UTILS_H_
#define _UTILS_H_


// ============================================================================
//! \page utilitiesPage Utilities documentation
//! \tableofcontents
//! The utilities library provides an implementation of various general concepts.
//!
//! \section secU1 General utilities
//! The "Utils" utilities provide:
//! - generic callback classes (for 'free' function or class function),
//! - a logging management (log manager, log catcher, basic logging functions),
//! - various string specializations (conversion functions, tokenized string, 
//! std::string extension, symbol evaluation),
//! - a signal object providing an observer pattern,
//! - generic objects counters (shared, weak counters),
//! - a non "copyable" class,
//! - a singleton template,
//! - a Unified Resource Identifier object (for URL parsing).
//!
//!
//! \section secU2 Utilities classes
//! Links to utilities classes :
//!   - yat::CallbackContainer
//!   - yat::Callback
//!   - yat::FunctionCallbackContainer
//!   - yat::MemberCallbackContainer
//!   - yat::ILogTarget
//!   - yat::DefaultLogHandler
//!   - yat::LogManager
//!   - yat::LogForward
//!   - yat::LogCatcher
//!   - yat::Signal
//!   - yat::DefaultDeleter
//!   - yat::CountBase
//!   - yat::CountImpl
//!   - yat::SharedCounter
//!   - yat::WeakCounter
//!   - yat::String
//!   - yat::ISymbolInterpreter
//!   - yat::EnvVariableInterpreter
//!   - yat::StringTemplate
//!   - yat::StringTokenizer
//!   - yat::XString
//!   - yat::NonCopyable
//!   - yat::Singleton
//!   - yat::URI
// ============================================================================

#endif // _UTILS_H_
